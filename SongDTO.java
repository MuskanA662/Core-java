public class SongDTO {
	 private int no;
	 private String name;
	 private String singer;
	 
	 public void setNo(int no) {
		 this.no=no; 
	 }
	 public int getNo() {
		 return no;
	 }
	 public void setName(String name) {
		 this.name=name; 
	 }
	 public String getName() {
		 return name;
	 }
	 public void setSinger(String singer) {
		 this.singer=singer; 
	 }
	 public String getSinger() {
		 return singer;
	 }

}
